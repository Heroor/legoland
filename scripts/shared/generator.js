const getImports = libs =>
  libs
    .map(lib => {
      return `import ${lib.name} from '@${lib.type}/${lib.name}'`
    })
    .join('\n')

exports.generateEntryScript = function(libs) {
  const importsSrc = getImports(libs)
  const exportsSrc = libs.map(lib => lib.name).join(',\n  ')

  const resultSrc = `// Generated by cli
${importsSrc}

export default {
  ${exportsSrc},
}
`
  return resultSrc
}

exports.generateGlobalScript = function(libs) {
  const importsSrc = getImports(libs)
  const usesSrc = libs.map(lib => `Vue.use(${lib.name})`).join('\n')

  const resultSrc = `// Generated by cli
import Vue from 'vue'
${importsSrc}

${usesSrc}
`
  return resultSrc
}
